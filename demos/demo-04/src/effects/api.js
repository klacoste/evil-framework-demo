export const api = {
  search(query) {
    const normalized = query.trim().toLowerCase();
    const results = catalog
      .filter((item) => item.toLowerCase().includes(normalized))
      .map((item, index) => ({ id: `${normalized}-${index}`, label: item }));

    return new Promise((resolve) => {
      setTimeout(() => {
        resolve(results);
      }, delay);
    });
  },
};
